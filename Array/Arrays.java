package Array;
import java.util.Arrays.*;

public class Arrays {


    public static void main(String[] args) {
        int[] marks = new int[3];

        marks[0] = 50;
        marks[1] = 70;
        marks[2] = 93;

        int[] grades = {50, 55, 60, 70, 80};

        System.out.println(grades[0]);
        // Output: 50

        System.out.println(grades[4]);
        // Output: 80

    }
}
